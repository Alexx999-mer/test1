public class ex15 {  // Scrieti un program in care definiti 3 variabile in metoda “myVariables”, un int, un String si un boolean, apoi apelati metoda din metoda “main”
    public static void main(String[] args) {
        myVariables();
    }
    static void myVariables() {
       int x = 03;
       String y = "I LOVE YOU SO MUCH!";
       boolean z = true;
       System.out.println(x);
       System.out.println(y);
       System.out.println(z);
    }
}
